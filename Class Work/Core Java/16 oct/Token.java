class Token
{
	public static void main(String ar[])
	{
		int token=80;
		switch(token)
		{
			case 100: System.out.println("Jaint wheel");
			break;
			case 80: System.out.println("Tora Tora");
			break;
			case 50: System.out.println("Frog jump");
			break;
			case 20: System.out.println("Slide");
			break;
			default: System.out.println("Invalid token");
		}

		
	}
}