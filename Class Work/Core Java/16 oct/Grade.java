class Grade
{
	public static void main(String ar[])
	{
		int marks=10;
		if(marks>=80)
		{
			System.out.println("1st");	
		}
		else if(marks>=60)
		{
			System.out.println("2nd");	
		}
		else if(marks>=40)
		{
			System.out.println("3rd");	
		}
		else
		{
			System.out.println("fail");
		}
		
		
	}
}