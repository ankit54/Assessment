package com.manthan.array;

public class Demo2 {
	public static void main(String[] args) {
		
		String[] st= {"priya","chinnu","dimple","geeta"};
		for (int i = 0; i < st.length; i++) {
			System.out.println(st[i]);
		}
		
		
		System.out.println(".................................");
		
		
		for (int i = st.length-1; i >= 0; i--) {
			System.out.println(st[i]);
		}
		
		
	}
}
